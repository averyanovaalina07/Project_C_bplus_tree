#include <stdio.h>    // Подключаем стандартную библиотеку ввода-вывода
#include <stdlib.h>   // Подключаем библиотеку для базовых функций 
#include "bplus_tree.h" // Подключаем наш заголовочный файл с описанием структуры узла B+ дерева
#include "wal.h"      // Подключаем заголовочный файл для функций журналирования (Write-Ahead Logging)

// --- ПОИСК ---
// Функция ищет ключ и возвращает его значение, если ключ не найден, вернет -1
int search(int key) {
    // Начинаем спуск по дереву всегда с корневого узла
    int current_block = root_block_index;
    
    // Бесконечный цикл для спуска по дереву. Мы выйдем из него через return, когда дойдем до листа
    while (1) {
        // Берем указатель на текущий узел прямо из массива, отображенного в память (mmap)
        BPlusNode* node = &mapped_tree[current_block];
        
        // Ставим блокировку на ЧТЕНИЕ (Read Lock). Это позволяет нескольким потокам искать данные параллельно.
        pthread_rwlock_rdlock(&node->rwlock);
        
        // Инициализируем счетчик для прохода по массиву ключей внутри узла
        int i = 0;
        // Идем вправо по массиву ключей, пока искомый ключ больше текущего
        while (i < node->num_keys && key > node->keys[i]) {
            i++; // Увеличиваем индекс
        }
        
        // Если текущий узел - лист (самый нижний уровень), значит тут лежат сами значения
        if (node->is_leaf) {
            // По умолчанию ставим результат -1 ("не найдено")
            int result = -1;
            
            // Если мы не вышли за границы массива и ключ совпал с искомым
            if (i < node->num_keys && node->keys[i] == key) {
                // Запоминаем найденное значение
                result = node->values[i];
            }
            
            // Обязательно снимаем блокировку чтения перед выходом из функции
            pthread_rwlock_unlock(&node->rwlock);
            // Возвращаем найденное значение (или -1)
            return result;
            
        } else {
            // Если мы во внутреннем узле, значения тут нет, ключи нужны только для навигации
            // Запоминаем индекс нужного дочернего блока, чтобы спуститься в него
            int next_block = node->children[i];
            
            // Снимаем блокировку с ТЕКУЩЕГО узла, так как мы идем ниже
            pthread_rwlock_unlock(&node->rwlock);
            
            // Обновляем текущий блок для следующей итерации цикла while
            current_block = next_block;
        }
    }
}

// --- ДИАПАЗОННЫЙ ЗАПРОС ---
// Ищет и выводит все пары, ключи которых попадают в промежуток от min_key до max_key
void range_query(int min_key, int max_key) {
    printf("Диапазон [%d, %d]: ", min_key, max_key);
    
    // Начинаем с корня дерева
    int current_block = root_block_index;
    
    // ЭТАП 1: Быстро спускаемся по внутренним узлам до первого нужного листа
    while (!mapped_tree[current_block].is_leaf) {
        // Берем текущий внутренний узел
        BPlusNode* node = &mapped_tree[current_block];
        // Блокируем его на чтение
        pthread_rwlock_rdlock(&node->rwlock);
        
        // Ищем индекс дочернего узла, куда нужно пойти, ориентируясь на min_key
        int i = 0;
        while (i < node->num_keys && min_key > node->keys[i]) i++;
        
        // Запоминаем путь вниз
        int next_block = node->children[i];
        // Разблокируем узел
        pthread_rwlock_unlock(&node->rwlock);
        // Переходим к дочернему узлу
        current_block = next_block;
    }
    
    // ЭТАП 2: Теперь мы в листе. Идем вправо по связанному списку листьев
    // Идем, пока индекс текущего блока не станет равен -1 (конец списка)
    while (current_block != -1) {
        // Получаем текущий листовой узел
        BPlusNode* leaf = &mapped_tree[current_block];
        // Блокируем его на чтение
        pthread_rwlock_rdlock(&leaf->rwlock);
        
        // Проверяем все ключи в этом листе
        for (int i = 0; i < leaf->num_keys; i++) {
            // Если ключ попадает в наш диапазон
            if (leaf->keys[i] >= min_key && leaf->keys[i] <= max_key) {
                // Печатаем значения
                printf("(%d: %d) ", leaf->keys[i], leaf->values[i]);
            }
            // Если мы встретили ключ больше максимума
            if (leaf->keys[i] > max_key) {
                // Снимаем блокировку
                pthread_rwlock_unlock(&leaf->rwlock);
                printf("\n");
                return;
            }
        }
        
        // Если мы проверили весь лист, но максимум еще не достигнут,
        // запоминаем индекс соседа справа по двусвязному списку
        int next_block = leaf->next_leaf;
        
        // Отпускаем текущий лист
        pthread_rwlock_unlock(&leaf->rwlock);
        // Переходим к правому соседу
        current_block = next_block;
    }
    // Если дошли до конца всех листьев, просто печатаем перенос строки
    printf("\n");
}

// --- ВСТАВКА ---
// Упрощенная версия вставки (без разделения переполненных узлов - split)
void insert(int key, int value) {
    // Сначала пишем операцию в лог (WAL) до изменения данных в памяти!
    // Это гарантирует восстановление базы при сбое
    write_wal("INSERT", key, value);
    
    // Берем корневой узел
    int current_block = root_block_index;
    BPlusNode* root = &mapped_tree[current_block];
    
    // Ставим блокировку на запись (Write Lock)
    // Пока мы пишем, никто другой не может читать или писать в этот узел!
    pthread_rwlock_wrlock(&root->rwlock);
    
    // Если корень все еще является листом (дерево маленькое)
    if (root->is_leaf) {
        // Идем с конца массива (как в алгоритме сортировки вставками)
        int i = root->num_keys - 1;
        
        // Сдвигаем все ключи, которые больше нашего, на одну позицию вправо
        while (i >= 0 && root->keys[i] > key) {
            root->keys[i+1] = root->keys[i];     // Сдвиг ключа
            root->values[i+1] = root->values[i]; // Сдвиг значения
            i--; // Идем к предыдущему
        }
        
        // Вставляем наш новый ключ в освободившуюся ячейку
        root->keys[i+1] = key;
        // Вставляем значение
        root->values[i+1] = value;
        // Увеличиваем счетчик ключей в узле
        root->num_keys++;
    }
    
    // Снимаем блокировку записи, разрешая другим потокам работать с узлом
    pthread_rwlock_unlock(&root->rwlock);
}

// --- УДАЛЕНИЕ ---
// Упрощенная логика удаления смещением (балансировка при удалении требует нетривиальной логики)
void delete_key(int key) {
    // 1. Обязательно журналируем операцию удаления в WAL
    write_wal("DELETE", key, 0);
    
    // Начинаем с корня
    int current_block = root_block_index;
    BPlusNode* root = &mapped_tree[current_block];
    
    // Блокируем узел на запись, так как мы будем изменять массивы
    pthread_rwlock_wrlock(&root->rwlock);
    
    // Если мы работаем с листом
    if (root->is_leaf) {
        // Переменная для сохранения позиции найденного ключа (-1 = не найден)
        int pos = -1;
        
        // Пробегаем по всем ключам узла
        for (int i = 0; i < root->num_keys; i++) {
            // Если нашли тот самый ключ
            if (root->keys[i] == key) {
                pos = i; // Запоминаем его позицию
                break;   // Выходим из цикла поиска
            }
        }
        
        // Если ключ действительно был в узле
        if (pos != -1) {
            // Запускаем цикл от найденной позиции до конца массива
            for (int i = pos; i < root->num_keys - 1; i++) {
                // Сдвигаем элементы влево, тем самым затирая удаленный ключ
                root->keys[i] = root->keys[i+1];
                root->values[i] = root->values[i+1];
            }
            // Уменьшаем счетчик ключей
            root->num_keys--;
        }
    }
    // Отпускаем блокировку записи
    pthread_rwlock_unlock(&root->rwlock);
}
