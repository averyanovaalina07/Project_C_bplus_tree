#include <stdio.h>    // Для базовых функций ввода-вывода
#include <stdlib.h>   // Для функции exit() и макроса EXIT_FAILURE
#include <fcntl.h>    // Для флагов открытия файлов
#include <unistd.h>   // Для системных вызовов POSIX
#include <sys/mman.h> // Для системного вызова mmap (отображение файла в память)
#include "wal.h"      // Подключаем наши объявления глобальных переменных диска
#include "bplus_tree.h" // Подключаем структуру узла B+ дерева

// Размер строкового буфера для записи одной операции в журнал
#define LOG_BUF_SIZE 256

// --- ОПРЕДЕЛЕНИЕ ГЛОБАЛЬНЫХ ПЕРЕМЕННЫХ ---
// Дескриптор файла базы данных дерева
int tree_fd;
// Дескриптор лог-файла журнала WAL
int wal_fd;
// Инициализируем мьютекс для обеспечения атомарности записи в лог
pthread_mutex_t wal_mutex = PTHREAD_MUTEX_INITIALIZER; 

// Указатель на начало памяти, куда мы отобразим наш файл дерева (работает как массив узлов)
BPlusNode* mapped_tree;
// Индекс корневого узла (в нашем файле он всегда лежит в самом начале, под индексом 0)
int root_block_index = 0;
// Индекс следующего свободного блока для создания новых узлов (0 уже занят корнем)
int next_free_block = 1;
// Порядок дерева t (задается при старте системы)
int t_order = 3;

// --- ФУНКЦИЯ ЗАПИСИ В ЖУРНАЛ ---
// Вызывается ПЕРЕД каждым изменением дерева для обеспечения восстановления после сбоя
void write_wal(const char* operation, int key, int value) {
    // Запираем мьютекс: только один поток может писать в лог прямо сейчас, чтобы строчки не перемешались
    pthread_mutex_lock(&wal_mutex);
    
    // Создаем буфер для формирования текстовой строки лога
    char buffer[LOG_BUF_SIZE];
    // Форматируем строку: пишем название операции, ключ и значение (snprintf возвращает длину итоговой строки)
    int len = snprintf(buffer, sizeof(buffer), "%s %d %d\n", operation, key, value);
    // Выполняем системный вызов записи этой строки в файл журнала
    write(wal_fd, buffer, len);
    
    // Принудительно сбрасываем кэши операционной системы на жесткий диск
    fsync(wal_fd); 
    
    // Отпираем мьютекс, разрешая другим потокам делать свои записи в лог
    pthread_mutex_unlock(&wal_mutex);
}

// --- ИНИЦИАЛИЗАЦИЯ ФАЙЛОВ И ПАМЯТИ ---
void init_storage(int t, const char* data_file, const char* wal_file) {
    // Сохраняем заданный порядок дерева
    t_order = t;
    
    // Открываем лог-файл: только для записи (O_WRONLY), создать если нет (O_CREAT), всегда писать в конец (O_APPEND)
    wal_fd = open(wal_file, O_WRONLY | O_CREAT | O_APPEND, 0666);
    // Если ОС вернула -1, значит произошла ошибка доступа
    if (wal_fd == -1) {
        perror("Ошибка при открытии wal.log"); // Выводим системную ошибку
        exit(EXIT_FAILURE); // Экстренно завершаем программу
    }
    
    // Открываем файл дерева: для чтения и записи (O_RDWR), создать если нет (O_CREAT)
    tree_fd = open(data_file, O_RDWR | O_CREAT, 0666);
    if (tree_fd == -1) {
        perror("Ошибка при открытии tree.dat");
        exit(EXIT_FAILURE);
    }
    
    // Задаем файлу дерева фиксированный размер: 1000 блоков по 4096 байт
    ftruncate(tree_fd, MAX_BLOCKS * BLOCK_SIZE);
    
    // Отображаем файл в оперативную память (mmap)
    // MAP_SHARED означает, что изменения в памяти будут транслироваться в дисковый файл
    mapped_tree = (BPlusNode*) mmap(NULL, MAX_BLOCKS * BLOCK_SIZE, PROT_READ | PROT_WRITE, MAP_SHARED, tree_fd, 0);
    // Проверяем, успешно ли прошло отображение
    if (mapped_tree == MAP_FAILED) {
        perror("Ошибка mmap");
        exit(EXIT_FAILURE);
    }
    
    // Если мы только что создали файл (или он был пуст), то количество ключей в нулевом блоке будет 0
    // Значит, нам нужно проинициализировать корневой узел дерева
    if (mapped_tree->num_keys == 0) {
        mapped_tree->is_leaf = 1;        // Сначала корень является единственным листовым узлом
        mapped_tree->num_keys = 0;       // В нем пока нет данных
        mapped_tree->next_leaf = -1;     // Соседей справа нет (конец двусвязного списка)
        mapped_tree->prev_leaf = -1;     // Соседей слева нет
        // Инициализируем read-write блокировку (rwlock) специально для этого корневого узла
        pthread_rwlock_init(&mapped_tree->rwlock, NULL);
    }
}

// --- КОРРЕКТНОЕ ЗАКРЫТИЕ РЕСУРСОВ ---
// Вызывается перед самым завершением программы для корректного освобождения ресурсов
void cleanup_storage() {
    // Отключаем отображение памяти. ОС сама безопасно допишет оставшиеся изменения из оперативной памяти на диск.
    munmap(mapped_tree, MAX_BLOCKS * BLOCK_SIZE);
    // Закрываем дескриптор файла дерева
    close(tree_fd);
    // Закрываем дескриптор лог-файла журнала
    close(wal_fd);
}
